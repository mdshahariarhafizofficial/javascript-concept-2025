let numbers = [ 20, 10, 40, 50, 85 ];
console.log(numbers);


// Add elements in array from last
console.log("PUSH");

numbers.push(10000);
numbers.push(500000, 100000000 )
console.log(numbers);

// Remove elements in array from last
console.log("POP");
numbers.pop();
console.log(numbers);
numbers.pop();
console.log(numbers);

// Add element in array from start
console.log("UNShift");

numbers.unshift(80000);
numbers.unshift(0)
console.log(numbers);

// Remove elements in array from start
console.log("SHIFT");

numbers.shift();
console.log(numbers);

numbers.shift();
console.log(numbers);








